# POM -> Page object model -> It is design pattern where objects of a page are kept separately.
# It will be kept inside a class (page name) and operations performed will be created using a method.
# All the locators of the page will be written as a class variable.
# advantage :->
# Easier maintainability of the element locators
# Any update can be done directly at a particular page
# Easier reading of the steps.




